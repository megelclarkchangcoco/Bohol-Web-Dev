                                    <h2 class="text-uppercase">Sinulog Festival</h2>
                                    <img class="img-fluid d-block mx-auto" src="assets/img/portfolio/3.jpg" alt="..." />
                                    <p>Millions of people participated in Cebu City last Sunday to celebrate the Sinulog Festival, one of the most famous festivals in the Philippines. It was attended, not only by Filipinos, but people coming from different parts of the globe also went to witness the celebration. The magnificence of the festival made Filipinos, as well as our fellow Asians, refer to it as the “Mother of all Festivals”. From the colorful and well-made costumes of the dancers, to the beautiful dances and to the music brought by the drums, trumpets, and the native gongs, the Sinulog Festival never ceases to amaze its audience. For 32 years, the Sinulog Festival is a traditional celebration in Cebu City held every third Sunday of January to honor the Santo Niño (Child Jesus). Basically, the festival is done by a dance ritual, in which it tells the story of the Filipino people’s pagan past and their acceptance of Christianity. The word “Sinulog” means “graceful dance”, wherein it all started in 1980 with a simple dance that represents the “sulog” (or current) of a river in Cebu. With the celebration’s evolution, the Sinulog Festival has become a month long tradition in Cebu. The most-awaited feature, nevertheless, is the Sinulog Grand Parade which was held on the 15th day of January. “One Beat, One Dance, One Vision. Cebu – City of Culture” was the theme adopted for the 2012 Sinulog Festival. As the festival becomes more and more famous each year, tourists would never want to miss the opportunity to watch it and became more and more commercialized. The festival now, not only consists of costumes and dances, but also contests, such as art exhibits, beauty pageants, photo contests, and singing and dancing contests. Participants may also join the festival’s activities, such as concerts, cultural shows, fireworks display, fun runs, and street fairs. For the past 18 years, it has made the Filipinos residing in New Zealand celebrate their own version each year, a proof on how big of an influence the festival is. It has also been recognized as the “City of Culture” by the Association of Southeast Asian Nations (ASEAN). The 2012 Sinulog festival embraced the perks of technology, as it was broadcasted live in a major television and, for all the people in the world to see, it was also streamed online. With the overwhelming amount of people who wish to witness the traditional celebration and the beauty of the culture it depicts, this grand festival remains as Cebu’s proudest attraction, along with the beautiful beaches and historical places.</p>
