                                    <p>Ride a cab to Lapu-Lapu City and head on to our last stop – the Mactan Shrine! Built to commemorate Philippine’s first battle, the Mactan Shrine is Cebu’s tribute to Datu Lapu-Lapu for defending Cebu from the invading Spaniards, led by Ferdinand Magellan, in the Battle of Mactan that happened on April 27, 1521. To pay homage to both historical figures, the Mactan Shrine holds the Lapu-Lapu Shrine and the Magellan Shrine.  
