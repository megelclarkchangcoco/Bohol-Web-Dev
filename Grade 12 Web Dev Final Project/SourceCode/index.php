                                    <p>Cebu City Island, south-central Philippines. Located on Cebu Island’s eastern coast, it is protected by offshore Mactan Island and by the inland Cordillera Central. It is one of the country’s largest cities and is a bustling port. Its harbour is provided by the sheltered strait between Mactan Island and the coast. Cyberlordmaster The country’s oldest settlement, it is also one of its most historic and retains much of the flavour of its long Spanish heritage. A thriving port occupied the site when Ferdinand Magellan, the Portuguese navigator and explorer, landed there on April 7, 1521. He sealed a blood compact with Humabon, the chief of Cebu, but was killed later by Chief Lapulapu of nearby Mactan Island. On April 27, 1565, Miguel López de Legazpi and the friar Andrés de Urdaneta arrived on Cebu and founded the first Spanish settlement and Catholic mission in the Philippine archipelago. For six years, until Legazpi’s removal to Manila, Cebu was the Spanish colonial capital. It remained the primary Spanish bastion in the southern part of the Philippines. The cultural and commercial core of the central Visayan region, Cebu was opened to foreign trade in 1860. It was chartered as a city in 1936. Although it imports few foreign goods, it is the main collection centre for such interisland commodities as copra, abaca, sugar, timber, and fish. Cebu is a major point of passenger traffic by air and sea and is served by an international airport across the harbour on Mactan Island. Many Manila-based industrial and commercial firms maintain branches in Cebu City. Warehousing and assembly plants for wholesale trade are important to the economy. Textiles, footwear, processed foods, vegetable oil, furniture, and chemicals are leading products. Other manufactures include cosmetics, candles, pearl and aquamarine jewelry, and sistas (guitars and ukuleles), the latter primarily made on Mactan Island. The city is easily accessible from all points on Cebu Island. A coastal railway reaches from Cebu City north to Danao and south to Carcar, and highways cross the nearby Cordillera Central. During World War II, the city was almost destroyed by the Japanese in May 1942, but the port was left intact. The city was subsequently rebuilt and enlarged. Its layout follows the configuration of the shoreline, with the main business district adjoining the port area. Urban residents are concentrated nearby, and population influx has contributed to a housing shortage. Suburbs are located to the north and south along the coastal plain. Cebu City is a Roman Catholic archbishopric and is a centre of education. It is the site of five major universities: the University of San Carlos (1595), Cebu Institute of Technology (1946), Southwestern University (1946), University of the Southern Philippines (1927), and University of the Visayas (1919). The ruins of the Spanish Fort San Pedro are near the harbour.</p>
