Adjacent to the Lapu-Lapu Shrine is the Magellan Shrine which shelters the Magellan Monument and the Magellan Marker. Built in 1866, the Magellan Monument is a memorial tower that holds different inscriptions on each side, including Magellan’s name in Portuguese. On the other hand, the Magellan Marker is a panoramic painting of the Battle of Mactan that was placed in 1941. The Shrine is located along Punta Engaño Road, Lapu-Lapu City. Entrance is free but donations are welcomed.</p>
